class Vehicle{
    constructor(placa, tipo, propietario, marca, modelo, chasis, motor, capacidadCarga, foto, estaActivo, usuario){
        this.placa = placa;
        this.tipo = tipo;
        this.propietario = propietario;
        this.marca = marca;
        this.modelo = modelo;
        this.chasis = chasis;
        this.motor = motor;
        this.capacidadCarga = capacidadCarga;
        this.foto = foto;
        this.estaActivo = estaActivo;
        this.usuario = usuario;
    }
}

export default Vehicle;